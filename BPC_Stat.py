# Break Statement

print("Break Statement")

for r in "break":
    if r == 'm':
        break
    print(r, end = '')


# Pass Statement

print("\n\n\nPass Statement")

if 8>5:
    pass

print("If block passed")


# Continue Statement

print("\n\n\nContinue Statement")

for t in "potato":
    if t == 't':
        continue
    print(t,end = '')